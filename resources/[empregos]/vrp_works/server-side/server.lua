-- UPDATE CHEST
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterServerEvent("paymentChest")
AddEventHandler("paymentChest",function(chest,itemGive,payment)
	local data = vRP.getSData(chest)
	local items = json.decode(data) or {}
	if data and items ~= nil then
		local initial = 0
		repeat
			initial = initial + 1
		until items[tostring(initial)] == nil or (items[tostring(initial)] and items[tostring(initial)].item == itemGive)
		initial = tostring(initial)
		
		if items[initial] == nil then
			items[initial] = { item = itemGive, amount = parseInt(payment) }
		elseif items[initial] and items[initial].item == itemGive then
			items[initial].amount = parseInt(items[initial].amount) + parseInt(payment)
		end
		vRP.setSData(chest,json.encode(items))
	end
end)
