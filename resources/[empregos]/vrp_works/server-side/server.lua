-- AMOUNTSERVICE
-----------------------------------------------------------------------------------------------------------------------------------------
function Hiro.amountService(service)
	local source = source
	if amount[source] == nil then
		amount[source] = math.random(works[service]["amountDelivery"][1],works[service]["amountDelivery"][2])
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- collectConsume
-----------------------------------------------------------------------------------------------------------------------------------------
function Hiro.collectConsume(service)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		for k,v in pairs(works[service]["paymentItem"]) do
			local random = math.random(v[1],v[2])
			if vRP.computeInvWeight(user_id) + vRP.itemWeightList(k)*parseInt(random) <= vRP.getBackpack(user_id) then
				vRP.giveInventoryItem(user_id,k,random,true)
			else
				TriggerClientEvent("Notify",source,"vermelho", "<b>Mochila</b> cheia.", 5000)
				return false
			end
		end
		vRP.upgradeStress(user_id,1)
		TriggerClientEvent("sounds:Private",source,'coin',0.5)
		return true			
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- CHECKPERM
-----------------------------------------------------------------------------------------------------------------------------------------
function Hiro.checkPermission(Service)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if works[Service]["perm"] == nil then
			return true
		end

		if vRP.hasPermission(user_id,works[Service]["perm"]) then
			return true
		end
	end

	return false
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- deliveryConsume
-----------------------------------------------------------------------------------------------------------------------------------------
function Hiro.deliveryConsume(service)
	Hiro.amountService(service)

	local source = source 
	local user_id = vRP.getUserId(source)
	if user_id then	
		if vRP.tryGetInventoryItem(user_id,works[service]["itemDelivery"],parseInt(amount[source])) then
			for k,v in pairs(works[service]["paymentCash"]) do
				local random = math.random(v[1],v[2]) * amount[source]
				if vRP.computeInvWeight(user_id) + vRP.itemWeightList(k)*parseInt(random) <= vRP.getBackpack(user_id) then
					vRP.giveInventoryItem(user_id,k,random,true)
				end
			end

			if works[service]["chest"] then
				TriggerEvent("paymentChest","chest:"..tostring(works[service]["chest"][1]),works[service]["chest"][2],parseInt(works[service]["chest"][3]))
			end

			TriggerClientEvent("sounds:Private",source,"coin",0.5)
			vRP.upgradeStress(user_id,1)
			amount[source] = nil
			return true
		else
			TriggerClientEvent("Notify",source,"amarelo", "Você precisa de <b>"..vRP.format(parseInt(amount[source])).."x "..vRP.itemNameList(works[service]["itemDelivery"]).."</b>.", 5000)
		end
		return false
	end
end
