-- PERMISSAO 
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.checkPermission(permiss)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if vRP.hasPermission(user_id,permiss) then
			return true
        else        
			return false
		end
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTMETHOD
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.paymentMethod(permiss)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		for k,v in pairs(works[permiss]["payment"]) do
			local random = math.random(v[1],v[2])
			if vRP.computeInvWeight(user_id) + vRP.itemWeightList(k)*parseInt(random) <= vRP.getBackpack(user_id) then
				vRP.giveInventoryItem(user_id,k,random,true)
			else
				TriggerClientEvent("Notify",source,"vermelho", "<b>Mochila</b> cheia.", 5000)
				return false
			end
		end
		vRP.upgradeStress(user_id,2)
		TriggerClientEvent("sounds:Private",source,'coins',0.5)
		return true
	end
end
